const uniquePathsWithObstacles = grid => {
  if (grid.length === 0 || grid[0].length === 0) {
    return 0;
  }

  const m = grid.length;
  const n = grid[0].length;

  // Create dp
  const dp = [];
  for (let i = 0; i < m; i++) {
    const row = [];
    for (let j = 0; j < n; j++) {
      row.push(0);
    }
    dp.push(row);
  }

  // make sure start is possible
  if (grid[0][0] === 1) {
    return 0;
  }

  // make sure there is an end
  if (grid[m - 1][n - 1] === 1) {
    return 0;
  }
  dp[0][0] = 1;

  // set valid first columns
  for (let i = 1; i < m; i++) {
    if (grid[i][0] === 0) {
      dp[i][0] = 1;
    } else {
      break;
    }
  }

  // set valid first rows
  for (let i = 1; i < n; i++) {
    if (grid[0][i] === 0) {
      dp[0][i] = 1;
    } else {
      break;
    }
  }

  for (let i = 1; i < m; i++) {
    for (let j = 1; j < n; j++) {
      dp[i][j] = grid[i][j] === 0 ? dp[i - 1][j] + dp[i][j - 1] : 0;
    }
  }

  return dp[m - 1][n - 1];
};
